package com.yazuo.erp.bes.exception;

import com.yazuo.erp.exception.ErpException;

/**
 * 后端服务模块异常
 */
public abstract class BesException extends ErpException {

	private static final String MODULE_NAME = "后端服务系统";

	public String getModuleName() {
		return MODULE_NAME;
	}

	public BesException() {
		super();
	}

	public BesException(String message) {
		super(message);
	}

	public BesException(String message, Throwable cause) {
		super(message, cause);
	}

	public BesException(Throwable cause) {
		super(cause);
	}

}

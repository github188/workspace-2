package com.yazuo.erp.train.exception;

import com.yazuo.erp.exception.ErpException;

/**
 * 培训模块异常
 */
public abstract class TrainException extends ErpException {

    private static final String MODULE_NAME = "培训系统";

    public String getModuleName() {
        return MODULE_NAME;
    }

    public TrainException() {
        super();
    }

    public TrainException(String message) {
        super(message);
    }

    public TrainException(String message, Throwable cause) {
        super(message, cause);
    }

    public TrainException(Throwable cause) {
        super(cause);
    }

}

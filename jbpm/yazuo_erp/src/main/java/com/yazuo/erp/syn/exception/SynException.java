package com.yazuo.erp.syn.exception;

import com.yazuo.erp.exception.ErpException;

/**
 * 前端服务模块异常
 */
public abstract class SynException extends ErpException {

	private static final String MODULE_NAME = "同步数据模块";

	public String getModuleName() {
		return MODULE_NAME;
	}

	public SynException() {
		super();
	}

	public SynException(String message) {
		super(message);
	}

	public SynException(String message, Throwable cause) {
		super(message, cause);
	}

	public SynException(Throwable cause) {
		super(cause);
	}

}

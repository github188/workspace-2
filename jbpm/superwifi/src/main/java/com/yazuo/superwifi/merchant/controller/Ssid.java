package com.yazuo.superwifi.merchant.controller;

public class Ssid
{

    Integer id;
    String name;
    String portal_addr;
    Integer portal_status;
    Integer enable_ssid_authen;
    Integer encrypt_mode;
    String password;
    public Integer getId()
    {
        return id;
    }
    public void setId(Integer id)
    {
        this.id = id;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public String getPortal_addr()
    {
        return portal_addr;
    }
    public void setPortal_addr(String portal_addr)
    {
        this.portal_addr = portal_addr;
    }
    public Integer getPortal_status()
    {
        return portal_status;
    }
    public void setPortal_status(Integer portal_status)
    {
        this.portal_status = portal_status;
    }
    public Integer getEnable_ssid_authen()
    {
        return enable_ssid_authen;
    }
    public void setEnable_ssid_authen(Integer enable_ssid_authen)
    {
        this.enable_ssid_authen = enable_ssid_authen;
    }
    public Integer getEncrypt_mode()
    {
        return encrypt_mode;
    }
    public void setEncrypt_mode(Integer encrypt_mode)
    {
        this.encrypt_mode = encrypt_mode;
    }
    public String getPassword()
    {
        return password;
    }
    public void setPassword(String password)
    {
        this.password = password;
    }
}
